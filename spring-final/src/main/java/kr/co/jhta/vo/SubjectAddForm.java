package kr.co.jhta.vo;

public class SubjectAddForm {
	private String subjeckName;
	private String profname;
	private String code;
	private int selectNo;
	private int grade;
	private String isPassed;
	private int score;
	public String getSubjeckName() {
		return subjeckName;
	}
	public void setSubjeckName(String subjeckName) {
		this.subjeckName = subjeckName;
	}
	public String getProfname() {
		return profname;
	}
	public void setProfname(String profname) {
		this.profname = profname;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getSelectNo() {
		return selectNo;
	}
	public void setSelectNo(int selectNo) {
		this.selectNo = selectNo;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public String getIsPassed() {
		return isPassed;
	}
	public void setIsPassed(String isPassed) {
		this.isPassed = isPassed;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
	
}
